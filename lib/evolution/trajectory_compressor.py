"""Trajectory compressor — Hermes-style middle-turn summarization.

Input: a `TrajectoryRecord` produced by `TrajectoryRecorder`.
Output: a new `TrajectoryRecord` with the same first/last turns preserved
and middle turns replaced by a single `[CONTEXT SUMMARY]:` turn
generated by an LLM (default Gemini Flash).

This mirrors Hermes `trajectory_compressor.py`:
- Protected head: first system + first human + first assistant + first tool
- Protected tail: last 4 turns
- Middle: collapsed into one summary turn

Compression is skipped if the trajectory is already short (<= head+tail)
or if no summary callable is wired up (offline/no-API mode).
"""

from __future__ import annotations

import logging
from typing import Callable

from schemas.trajectory import TrajectoryRecord, Turn

log = logging.getLogger("mira.evolution.compressor")

# Summary marker — downstream reflect prompts recognize this prefix so
# they don't treat the summary as a real turn.
SUMMARY_PREFIX = "[CONTEXT SUMMARY]: "

# Default tail size — keep the last N turns verbatim.
DEFAULT_TAIL = 4

# Protected head roles — the compressor keeps the first occurrence of each.
_HEAD_ROLES = ("system", "human", "assistant", "tool")


SummarizeFn = Callable[[str], str]


def _default_summarizer() -> SummarizeFn | None:
    """Return a Gemini Flash summarizer, or None if unavailable.

    Kept lazy so importing this module is safe even when the LLM
    package is missing.
    """
    try:
        from llm_providers.gemini import gemini_think  # type: ignore
    except Exception as e:  # pragma: no cover - depends on provider setup
        log.debug("Gemini unavailable for compressor: %s", e)
        return None

    def _summarize(text: str) -> str:
        prompt = (
            "Summarize the following agent conversation segment. Include: "
            "(1) what actions the assistant took (tool calls, searches, file ops), "
            "(2) key information or results obtained, "
            "(3) important decisions, "
            "(4) specific filenames/values/identifiers worth preserving. "
            "Keep under 300 tokens. Reply in the same language as the input.\n\n"
            f"{text}"
        )
        try:
            return gemini_think(prompt, model="gemini-2.5-flash", timeout=45) or ""
        except Exception as e:
            log.warning("Gemini summarize failed: %s", e)
            return ""

    return _summarize


def _slice_protected(conversations: list[Turn], tail: int) -> tuple[list[Turn], list[Turn], list[Turn]]:
    """Return (head_protected, middle_candidates, tail_protected).

    Head: first occurrence of each role in _HEAD_ROLES, in original order.
    Tail: last `tail` turns not already in head.
    Middle: everything else.
    """
    head_indices: list[int] = []
    seen_roles: set[str] = set()
    for idx, turn in enumerate(conversations):
        if turn.role in _HEAD_ROLES and turn.role not in seen_roles:
            head_indices.append(idx)
            seen_roles.add(turn.role)
        if len(seen_roles) == len(_HEAD_ROLES):
            break
    head_set = set(head_indices)

    total = len(conversations)
    tail_start = max(total - tail, 0)
    tail_indices = [i for i in range(tail_start, total) if i not in head_set]
    tail_set = set(tail_indices)

    middle_indices = [i for i in range(total) if i not in head_set and i not in tail_set]

    head_turns = [conversations[i] for i in head_indices]
    middle_turns = [conversations[i] for i in middle_indices]
    tail_turns = [conversations[i] for i in tail_indices]
    return head_turns, middle_turns, tail_turns


def _format_middle_for_summary(turns: list[Turn]) -> str:
    """Render middle turns as a compact text block for the summarizer."""
    lines: list[str] = []
    for t in turns:
        prefix = t.role.upper()
        if t.tool_name:
            prefix = f"{prefix}/{t.tool_name}"
        text = (t.content or "").strip()
        if t.tool_result_preview:
            text = f"{text}\n[tool_result] {t.tool_result_preview}"
        lines.append(f"{prefix}: {text}")
    return "\n\n".join(lines)


def compress(
    trajectory: TrajectoryRecord,
    *,
    tail: int = DEFAULT_TAIL,
    summarizer: SummarizeFn | None = None,
) -> TrajectoryRecord:
    """Return a compressed copy of `trajectory`.

    Never mutates the input. Returns the original object unchanged
    when compression isn't applicable (too short, no summarizer).
    """
    turns = trajectory.conversations
    head, middle, tail_turns = _slice_protected(turns, tail)
    if not middle:
        return trajectory

    summarizer = summarizer or _default_summarizer()
    if summarizer is None:
        # No LLM available — still drop middle but record that we did so.
        summary_content = f"{SUMMARY_PREFIX}[{len(middle)} turns omitted — no summarizer available]"
    else:
        rendered = _format_middle_for_summary(middle)
        summary = summarizer(rendered).strip()
        if not summary:
            # Summarizer failed; keep original to avoid lossy drop without signal.
            return trajectory
        summary_content = f"{SUMMARY_PREFIX}{summary}"

    summary_turn = Turn(role="system", content=summary_content)
    new_conversations = [*head, summary_turn, *tail_turns]

    compressed = trajectory.model_copy(
        update={
            "conversations": new_conversations,
            "compressed": True,
            "original_turn_count": len(turns),
        },
        deep=True,
    )
    return compressed
